package org.s23m.cell.serialization.serializer;

import org.s23m.cell.serialization.InstanceType;

public final class InstanceMapValue {
	
	private InstanceType instance;
	private String id;
	private String artifact;
	private String metaElement;
	private InstanceBuildType buildType;
	
	protected InstanceType getInstance() {
		return instance;
	}

	protected InstanceBuildType getBuildType() {
		return buildType;
	}

	protected InstanceMapValue(InstanceType instance, InstanceBuildType buildType) {
		this.instance = instance;
		this.buildType = buildType;
		this.id = instance.getId();
		this.artifact = instance.getArtifact();
		this.metaElement = instance.getMetaElement();
	}

	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		InstanceMapValue other = (InstanceMapValue) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}

    
}
