package org.s23m.cell.common.search;

import java.util.UUID;

public class BasicSearchIdentity {

	private final String name;
	private final String pluralName;
	private final UUID uuid;

	public BasicSearchIdentity(final String name, final String pluralName, final UUID uuid) {
		super();
		this.name = name;
		this.pluralName = pluralName;
		this.uuid = uuid;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((uuid == null) ? 0 : uuid.hashCode());
		return result;
	}

	@Override
	public boolean equals(final Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		final BasicSearchIdentity other = (BasicSearchIdentity) obj;
		if (uuid == null) {
			if (other.uuid != null) {
				return false;
			}
		} else if (!uuid.equals(other.uuid)) {
			return false;
		}
		return true;
	}

	public String getName() {
		return name;
	}

	public String getPluralName() {
		return pluralName;
	}

	public UUID getUUID() {
		return uuid;
	}

}
